/* Прекомпьют 4 поворотов каждой фигуры.
   Поворот CW при ряде-вниз: (c, r) -> (size-1-r, c). */
NT.PIECES.forEach(function (p) {
  p.rots = [p.cells];
  for (var i = 0; i < 3; i++) {
    p.rots.push(p.rots[i].map(function (cr) { return [p.size - 1 - cr[1], cr[0]]; }));
  }
});

